"""Tool definitions for the command generator. All 95 tools, 3 tiers."""

# ── Compact builder helpers ────────────────────────────────────────────────────

def T(flag, doc):
    """Checkbox toggle — adds the flag when checked."""
    return {"id": flag.lstrip("-").replace("-", "_"), "label": flag,
            "type": "checkbox", "flag": flag, "doc": doc}

def F(fid, flag, label, doc, ph="", typ="text"):
    """Text/number field — adds `flag value` when non-empty."""
    return {"id": fid, "flag": flag, "label": label, "type": typ,
            "placeholder": ph, "doc": doc}

def Sel(fid, flag, label, doc, opts):
    """Select dropdown. opts = list of (display_label, cli_value) tuples."""
    return {"id": fid, "flag": flag, "label": label, "type": "select",
            "options": [{"label": l, "value": v} for l, v in opts], "doc": doc}

def Tgt(ph, doc="Target host, IP, URL, or domain"):
    """Positional target appended at end of command with no flag prefix."""
    return {"id": "target", "label": "Target", "type": "text", "flag": "",
            "position": "last", "placeholder": ph, "doc": doc, "req": True}

def S(sid, label, icon, *fields):
    """Section grouping."""
    return {"id": sid, "label": label, "icon": icon, "fields": list(fields)}

def P(label, vals):
    """Preset dict mapping field_id → value."""
    return {"label": label, "values": vals}

def _opts(*pairs):
    """Flatten (label, val) pairs into option list with empty first entry."""
    return [("(not set)", "")] + list(pairs)


